    <div class="col">
        <p>1. W&nbsp;najbliższy czwartek – Uroczystość Objawienia Pańskiego
        – w&nbsp;tradycji: Święto Trzech Króli – prządek Mszy&nbsp;św., jak
        w&nbsp;niedzielę. Poświęcenie kredy i&nbsp;kadzidła
        na&nbsp;Mszach&nbsp;św. Poświęconą kredą oznaczamy odrzwia naszych
        domów i&nbsp;mieszkań <strong>C+M+B 2022</strong>.
        O&nbsp;godz.&nbsp;12.00 w&nbsp;tym dniu wyruszy od&nbsp;naszego
        kościoła orszak z&nbsp;jednym z&nbsp;„Trzech Króli”. Wspólnie udamy
        się ze&nbsp;śpiewem kolęd do&nbsp;stajenki na&nbsp;rynku Starego
        Miasta, aby złożyć pokłon Nowonarodzonemu Królowi Świata. Zakończenie
        uroczystości o&nbsp;godz.&nbsp;13.30. Będzie gorąca herbata
        i&nbsp;pączek. Zapraszamy! Zachęcamy, aby tegoroczny Orszak miał
        także wymiar charytatywny, pragniemy połączyć go&nbsp;ze&nbsp;zbiórką
        darów materialnych, takich jak: chemia gospodarcza: proszki płyny
        do&nbsp;prania, mycia, czyszczenia; chemia osobista: żele pod
        prysznic, szampony, pasta do&nbsp;zębów; tekstylia: ręczniki, które
        trafią do&nbsp;dzieci, podopiecznych placówek
        opiekuńczo-wychowawczych w&nbsp;Janowie Lubelskim. Przy naszej szopce
        w&nbsp;kościele będą pojemniki, do&nbsp;których parafianie będą mogli
        złożyć wspomniane dary. <em>Bóg zapłać</em>.</p>
        <p>2. W&nbsp;tym roku nie będzie wizyty duszpasterskiej w&nbsp;żadnej
        formie.</p>
        <p>3. W&nbsp;tym tygodniu Pierwszy czwartek, piątek miesiąca.
        W&nbsp;Pierwszy Piątek Miesiąca, spowiedź w&nbsp;tym dniu
        od&nbsp;godz.&nbsp;17.00.</p>
        <p>4. Koncert kolęd Chóru Katedralnego dziś o&nbsp;godz.&nbsp;16.00
        w&nbsp;katedrze Sandomierskiej. Zapraszamy.</p>
        <p>5. W&nbsp;przyszłą niedzielę – Święto Chrztu Pańskiego. Koniec
        Okresu Bożego Narodzenia. Tradycyjnie bożonarodzeniową dekorację
        zachowujemy w&nbsp;kościele do&nbsp;Święta Ofiarowania Pańskiego –
        tradycyjnie – Matki Bożej Gromnicznej.</p>
        <p>6. Wdzięczni jesteśmy za&nbsp;modlitwę, dobre słowo, składane
        ofiary na&nbsp;potrzeby parafii. Wszystkim, którzy w&nbsp;tym
        tygodniu obchodzą swoje imieniny, urodziny, inne rocznice, składamy
        najlepsze życzenia z&nbsp;kapłańskim błogosławieństwem. Wszystkim
        uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy błogosławionej
        niedzieli.</p>
