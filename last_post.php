        <h3>3.&nbsp;lipca 2022&nbsp;r.</h3>
        <h3>XIV Niedziela Zwykła, Rok C</h3>
    </div>
    <div class="col">
        <p>1. Jutro, tj.&nbsp;4&nbsp;lipca, przypada 13&nbsp;Rocznica sakry
        i&nbsp;ingresu Ks.&nbsp;Bp.&nbsp;Krzysztofa Nitkiewicza,
        w&nbsp;naszej modlitwie liturgicznej i&nbsp;osobistej polecajmy Panu
        Bogu Pasterza Kościoła Sandomierskiego.</p>
        <p>2. Przed nami czas wakacji, przypominamy wszystkim katolikom
        o&nbsp;trwaniu w&nbsp;wierności wobec Boga i&nbsp;Jego przykazań,
        i&nbsp;zachowaniu szacunku do&nbsp;Kościoła&nbsp;św.,
        i&nbsp;przestrzegania Jego zasad, choćby systematyczne uczestnictwo
        w&nbsp;niedzielnej Mszy&nbsp;św. codzienna modlitwa, korzystanie
        za&nbsp;sakramentów&nbsp;św., godny strój i&nbsp;szlachetne
        zachowanie w&nbsp;miejscach&nbsp;św.</p>
        <p>3. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka.
        Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.
        Są&nbsp;czasopisma „Niedziela”, „Miłujcie się”, „Gość Niedzielny”
        i&nbsp;„Mały Gość Niedzielny”.</p>
        <p>4. W&nbsp;dniach od&nbsp;4 do&nbsp;12 sierpnia&nbsp;br. odbędzie
        się XXXIX Piesza Pielgrzymka Ziemi Sandomierskiej na&nbsp;Jasną Górę
        pod hasłem „Maryja znakiem nadziei i&nbsp;pokoju”, Pielgrzymka
        ma&nbsp;charakter wędrownych rekolekcji z&nbsp;programem różnych
        modlitw, konferencji i&nbsp;śpiewu. Jest zarazem jedną z&nbsp;dróg
        prowadzących do&nbsp;Pana Boga poprzez wstawiennictwo Maryi.
        Na&nbsp;pątniczy szlak zapraszamy ludzi dobrej woli, starszych
        i&nbsp;młodych. Zapisy w&nbsp;parafii. Wpisowe 150&nbsp;zł
        od&nbsp;osoby, druga osoba z&nbsp;rodziny 100&nbsp;zł, trzecia
        i&nbsp;następne 70&nbsp;zł. Z&nbsp;naszej parafii w&nbsp;pielgrzymce
        pójdzie dwóch kapłanów, przewodnikiem grupy sandomierskiej jest
        ks.&nbsp;Krystian Kałuża – wikariusz w&nbsp;naszej parafii. Dla osób,
        które z&nbsp;różnych przyczyn nie będą mogły wziąć udział
        w&nbsp;9-dniowej pielgrzymce będzie możliwość 1-dniowego sztafetowego
        pielgrzymowania (koszt: 20&nbsp;zł, dojazd we&nbsp;własnym zakresie.
        Więcej informacji u&nbsp;Księdza Krystiana. Zachęcamy również tych,
        którzy nie mogą wziąć udziału w&nbsp;pieszej pielgrzymce
        do&nbsp;udziału w&nbsp;duchowym pielgrzymowaniu i&nbsp;łączeniu się
        każdego dnia z&nbsp;pielgrzymami przez modlitwę.</p>
        <p>5. Dziękujemy za&nbsp;modlitwę w&nbsp;intencji kapłanów,
        za&nbsp;wszelką pomoc, prace porządkowe przy kościele, złożone ofiary
        na&nbsp;rzecz parafii. Wszystkim, którzy w&nbsp;tym tygodniu
        obchodzą swoje imieniny, urodziny, inne rocznice, składamy najlepsze
        życzenia. Wszystkim uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy
        błogosławionej niedzieli.</p>
