        ołtarzu.</p>
        <p>6. Polecamy prasę katolicką i&nbsp;zachęcamy do&nbsp;jej
        lektury.</p>
        <p>7. Dziękujemy za&nbsp;modlitwę w&nbsp;intencji kapłanów,
