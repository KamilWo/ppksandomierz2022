        pasjonujące”. O&nbsp;czym informowaliśmy w&nbsp;zeszłą niedziele.
        <em>Bóg zapłać</em> ofiarodawcom, niech Matka Boża Sulisławska
        ma&nbsp;was w&nbsp;swojej opiece.</p>
        <p>4. Dziękujemy rodzicom młodzieży bierzmowanej
        za&nbsp;przygotowanie kościoła do&nbsp;Uroczystości udzielenia
        sakramentu Bierzmowania młodzieży z&nbsp;naszej parafii, która
        odbędzie się jutro o&nbsp;godz.&nbsp;18.00. Sakramentu bierzmowania
        udzieli ks.&nbsp;Biskup Ordynariusz.</p>
        <p>5. Jutro też o&nbsp;godz.&nbsp;12.00 - Msza&nbsp;św.
        w&nbsp;intencji strażaków. Liturgii będzie przewodniczył będzie
        ks.&nbsp;Biskup Krzysztof Nitkiewicz. Zapraszamy do&nbsp;udziału
        w&nbsp;niej parafian, będzie to&nbsp;także wyraz naszej wdzięczności
        za&nbsp;ich służbę na&nbsp;rzecz dotkniętych cierpieniem.</p>
        <p>6. W&nbsp;przyszłą niedzielę o&nbsp;godz.&nbsp;12.30 będzie miała
        miejsce Uroczystość Pierwszokomunijna dzieci z&nbsp;naszej parafii.
        Spotkanie organizacyjne dla dzieci we&nbsp;wtorek 17.05
        o&nbsp;godz.&nbsp;16.00, natomiast pierwsza spowiedź dla nich
        w&nbsp;piątek 20.05 o&nbsp;godz.&nbsp;17.00.</p>
        <p>7. Dziękujemy parafianom za&nbsp;modlitwę, wszelką pomoc, składane
        ofiary na&nbsp;potrzeby parafii, przejawy szacunku i&nbsp;sympatii.
        Wszystkim życzymy błogosławionej Niedzieli.</p>
