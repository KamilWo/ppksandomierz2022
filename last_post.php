        <h3>31.&nbsp;lipca 2022&nbsp;r.</h3>
        <h3>XVIII Niedziela Zwykła, Rok C</h3>
    </div>
    <div class="col">
        <p>1. W&nbsp;dniach od&nbsp;4 do&nbsp;12 sierpnia&nbsp;br. odbędzie
        się XXXIX Piesza Pielgrzymka Ziemi Sandomierskiej na&nbsp;Jasną Górę.
        Zapisy na&nbsp;pielgrzymkę odbędą się przy kościele św.&nbsp;Pawła
        w&nbsp;Sandomierzu 3&nbsp;sierpnia w&nbsp;godzinach od&nbsp;10
        do&nbsp;17.00. bagaże składamy przy zapisach. Zapraszamy.</p>
        <p>2. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka.
        Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.</p>
        <p>3. Papieskie Stowarzyszenie Pomoc Kościołowi w&nbsp;Potrzebie
        prowadzi kompanie SOS dla Ziemi Świętej. Jej głównym celem jest pomoc
        i&nbsp;wyrażenie solidarności z&nbsp;wyznawcami Chrystusa
        na&nbsp;Jego ziemi. Od&nbsp;lat trwa konflikt pomiędzy palestyńskimi
        muzułmanami a&nbsp;Izraelczykami. Zbrojne starcia pochłonęły już
        wiele ofiar. Konsekwencje tego konfliktu dotykają również chrześcijan,
        którzy są&nbsp;prześladowani i&nbsp;dyskryminowani. Wyznawcy
        Chrystusa są&nbsp;zastraszani, a&nbsp;nawet wypędzani ze&nbsp;swoich
        własnych domów przez ekstremistów muzułmańskich. W&nbsp;kampanii SOS
        dla Ziemi Świętej proponujemy zakup wyrobów z&nbsp;drewna oliwnego,
        wykonanych przez chrześcijan z&nbsp;Betlejem. Nabywając wyroby
        wykonane przez naszych Braci i&nbsp;Siostry z&nbsp;Ziemi Świętej,
        dajemy im&nbsp;pracę i&nbsp;pomagamy godnie żyć. Siostra Karolina
        Mordaka – Dominikanka za&nbsp;chwilę więcej powie w&nbsp;tej
        kwestii.</p>
        <p>4. W&nbsp;przyszłą niedzielę w&nbsp;kościele, pół godziny przed
        Mszą&nbsp;św., która sprawowana jest o&nbsp;godz.&nbsp;11.15 –
        adoracja Najśw. Sakramentu połączona z&nbsp;modlitwą różańcową,
        którą poprowadzi Koło Żywego Różańca z&nbsp;naszej parafii
        i&nbsp;zmiana tajemnic różańcowych. Zapraszamy do&nbsp;wspólnotowej
        modlitwy.</p>
        <p>5. W&nbsp;tym tygodniu Pierwszy piątek, sobota miesiąca.
        W&nbsp;Pierwszy Piątek Miesiąca, spowiedź w&nbsp;tym dniu
        od&nbsp;godz.&nbsp;17.30.</p>
        <p>6. W&nbsp;najbliższą sobotę o&nbsp;godz.&nbsp;17.15. – nabożeństwo
        Różańcowe poprowadzi Legion Maryi z&nbsp;naszej parafii.</p>
        <p>7. Wiele polskich diecezji boryka się z&nbsp;problemami kadrowymi
        wśród katechetów. Brakuje ludzi do&nbsp;pracy na&nbsp;stanowisku
        nauczyciela religii. Po&nbsp;analizie dokonanej w&nbsp;Wydziale Nauki
        i&nbsp;Wychowania Katolickiego w&nbsp;Sandomierskiej Kurii
        Diecezjalnej możemy powiedzieć, że&nbsp;wciągu najbliższych
        10&nbsp;lat na&nbsp;emeryturę odejdzie około 100&nbsp;„świeckich”
        katechetów. Również wielu księży osiągnie wiek emerytalny.
        Te&nbsp;dane jasno pokazują, że&nbsp;musimy się przygotować
        na&nbsp;braki kadrowe nauczycieli religii w&nbsp;przyszłości.
        Od&nbsp;października br. chcemy rozpocząć nową edycję katechetycznych
        studiów podyplomowych, adresowanych do&nbsp;wszystkich mających
        wyższe wykształcenie (nie trzeba mieć przygotowania
        pedagogicznego!!!). Mogą z&nbsp;nich skorzystać zarówno osoby mające
        już wyższe wykształcenie z&nbsp;przygotowaniem pedagogicznym, jak
        i&nbsp;te, które posiadają dyplom magistra bez przygotowania
        pedagogicznego. Przyszli absolwenci naszych studiów uzyskają
        uprawnienia do&nbsp;nauczania religii we&nbsp;wszystkich typach
        szkół.</p>
        <p>8. Dziękujemy za&nbsp;modlitwę w&nbsp;intencji kapłanów,
        za&nbsp;wszelką pomoc, prace porządkowe przy kościele, złożone ofiary
        na&nbsp;rzecz parafii. Wszystkim, którzy w&nbsp;tym tygodniu obchodzą
        swoje imieniny, urodziny, inne rocznice, składamy najlepsze życzenia.
        Wszystkim uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy błogosławionej
        niedzieli.</p>
