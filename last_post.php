        <h3>27.&nbsp;lutego 2022&nbsp;r.</h3>
        <h3>VIII NIEDZIELA Zwykła, Rok C</h3>
    </div>
    <div class="col">
        <p>1. W&nbsp;dniach od&nbsp;27 do&nbsp;5&nbsp;marca br.
        w&nbsp;Kościele w&nbsp;Polsce przeżywamy Tydzień modlitw
        o&nbsp;trzeźwość Polaków. Tam, gdzie brakuje trzeźwości, tam
        pojawiają się w&nbsp;nadmiarze problemy, mnożą się dramaty całych
        rodzin, wspólnot, ale także i&nbsp;całych środowisk. Trzeźwość jest
        tamą, która chroni życie indywidualne, rodzinne, ale także społeczne
        przed wielką powodzią cierpienia. Módlmy się i&nbsp;troszczmy
        o&nbsp;trzeźwość.</p>
        <p>2. W&nbsp;tym tygodniu rozpoczynamy czas Wielkiego Postu,
        najbliższa środa to&nbsp;Środa Popielcowa. Msze&nbsp;św.
        z&nbsp;posypaniem głów popiołem o&nbsp;godz.&nbsp;6.30, 9.00
        i&nbsp;18.00. Przypominamy, że&nbsp;w&nbsp;tym dniu obowiązuje post
        ścisły ilościowy i&nbsp;jakościowy.
        <strong>Taca ze&nbsp;Środy Popielcowej przeznaczona jest
        na&nbsp;Caritas.</strong>
        W&nbsp;naszej Parafii rekolekcje wielkopostne rozpoczną się
        późniejszym terminie, tj. w&nbsp;piątek w&nbsp;Uroczystość
        św.&nbsp;Józefa, 3&nbsp;kwietnia br. w&nbsp;V&nbsp;niedzielę
        Wielkiego Postu.</p>
        <p>3. Droga Krzyżowa w&nbsp;każdy piątek Wielkiego Postu
        o&nbsp;godz.&nbsp;17.15 - zapraszamy do&nbsp;udziału.</p>
        <p>4. W&nbsp;dniu 12&nbsp;marca br. przypada czterechsetna rocznica
        święceń kapłańskich św.&nbsp;Andrzeja Boboli. Poprzedzi
        ją&nbsp;nowenna za&nbsp;kapłanów oraz w&nbsp;intencji powołań
        kapłańskich. Nowenna rozpocznie się dnia 3&nbsp;marca br.
        a&nbsp;zakończy w&nbsp;przededniu rocznicy święceń św.&nbsp;Andrzeja,
        tj.&nbsp;11&nbsp;marca br. i&nbsp;będzie miała miejsce
        w&nbsp;kościele seminaryjnym w&nbsp;Sandomierzu, początek tych
        modlitw we&nbsp;wspomniane dni od&nbsp;godz.&nbsp;20.30 kończyć będą
        się one Apelem Jasnogórskim. Mając na&nbsp;uwadze nieustanną potrzebę
        troski o&nbsp;powołania kapłańskie oraz znaczenie modlitwy
        w&nbsp;intencji kapłanów, zachęcamy parafian do&nbsp;włączenia się
        w&nbsp;tę&nbsp;szlachetną i&nbsp;ważną inicjatywę.</p>
        <p>5. W&nbsp;ramach przygotowania młodzieży do&nbsp;godnego przyjęcia
        Sakramentu Bierzmowania – młodzież z&nbsp;klasy szóstej, siódmej
        i&nbsp;ósmej z&nbsp;naszej parafii ma&nbsp;spotkanie
        w&nbsp;parafialnym kościele w&nbsp;najbliższy wtorek godz.&nbsp;17.00,
        a&nbsp;po&nbsp;nim spowiedź św.</p>
        <p>6. W&nbsp;przyszłą niedzielę w&nbsp;kościele, pół godziny przed
        Mszą&nbsp;św. która sprawowana jest o&nbsp;godz.&nbsp;11.15 –
        adoracja Najśw. Sakramentu połączona z&nbsp;modlitwą różańcową, którą
        poprowadzi Koło Żywego Różańca z&nbsp;naszej parafii i&nbsp;zmiana
        tajemnic różańcowych. Zapraszamy do&nbsp;wspólnotowej modlitwy.</p>
        <p>7. W&nbsp;tym tygodniu Pierwszy czwartek, piątek, sobota miesiąca.
        W&nbsp;Pierwszy Piątek Miesiąca, spowiedź w&nbsp;tym dniu
        od&nbsp;godz.&nbsp;17.00.</p>
        <p>8. W&nbsp;najbliższą sobotę o&nbsp;godz.&nbsp;17.15. – nabożeństwo
        Różańcowe poprowadzi Legion Maryi z&nbsp;naszej parafii.</p>
        <p>9. W&nbsp;każdą niedzielę Wielkiego Postu nabożeństwo „Gorzkich
        Żali” z&nbsp;nauką pasyjną o&nbsp;godz.&nbsp;17.30, następnie Msza
        Święta.</p>
        <p>10. Zachęcamy do&nbsp;nabycia i&nbsp;lektury katolickiej prasy.
        Gazety na&nbsp;stoliku pod chórem.</p>
        <p>11. Wszystkim uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy
        błogosławionej niedzieli.</p>
        <p>
