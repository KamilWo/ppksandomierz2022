        <h3>20.&nbsp;marca 2022&nbsp;r.</h3>
        <h3>III Niedziela Wielkiego Postu, Rok C</h3>
    </div>
    <div class="col">
        <p>1. Słowa wdzięczności kierujemy do&nbsp;parafian za&nbsp;złożone
        ofiary na&nbsp;rzecz uchodźców z&nbsp;Ukrainy, na&nbsp;wyposażenie
        mieszkania, w&nbsp;którym przebywają, w&nbsp;budynku obok plebanii,
        tj.&nbsp;5126&nbsp;zł i&nbsp;na&nbsp;rzecz misji 1989&nbsp;zł.
        Dziękujemy za&nbsp;dary w&nbsp;postaci żywności, środków czystości
        i&nbsp;opatrunków. Serdeczne <em>Bóg zapłać</em>.</p>
        <p>2. W&nbsp;przyszłą niedzielę wizytacja kanoniczna w&nbsp;naszej
        parafii. Ks.&nbsp;Biskup Ordynariusz spotka się z&nbsp;Radą
        Parafialną na&nbsp;plebanii o&nbsp;godz.&nbsp;11.00, potem
        w&nbsp;kościele o&nbsp;godz.&nbsp;12.00, z&nbsp;poszczególnymi
        grupami duszpasterskim: Legionem Maryi, z&nbsp;wszystkimi Różami
        Różańcowymi z&nbsp;całej parafii, z&nbsp;Kościołem Domowym,
        z&nbsp;Rodziną Szkaplerzną, Służbą Ołtarza i&nbsp;innymi.
        O&nbsp;12.30 - Msza&nbsp;św. w&nbsp;intencji naszej parafii.
        Zapraszamy.</p>
        <p>3. Serdecznie zapraszamy wszystkich zainteresowanych
        na&nbsp;wykłady otwarte z&nbsp;filozofii. Wykład odbędzie się
        w&nbsp;sobotę, tj.&nbsp;26&nbsp;marca br. o&nbsp;godz.&nbsp;16.00
        w&nbsp;Sali Papieskiej Instytutu Teologicznego, ul.&nbsp;Mariacka 9.
        Temat wykładu: O&nbsp;wojnie sprawiedliwej i&nbsp;niesprawiedliwej.
        (prowadzący ks.&nbsp;dr&nbsp;Bartłomiej Krzos).</p>
        <p>4. W&nbsp;Uroczystość Zwiastowania Pańskiego, 25&nbsp;marca,
        kościół w&nbsp;Polsce obchodzi dzień Świętości Życia. Jego celem jest
        budzenie wrażliwości na&nbsp;sens i&nbsp;wartość ludzkiego życia,
        a&nbsp;każdym jego etapie oraz zwrócenie uwagi na&nbsp;potrzebę
        szczególnej troski o&nbsp;nie. Wyrazem takiej troski jest m.in.
        podjęcie Duchowej Adopcji dziecka Poczętego, czyli zobowiązanie się
        do&nbsp;modlitwy w&nbsp;intencji dziecka zagrożonego zabiciem
        w&nbsp;łonie matki. Zachęcamy do&nbsp;włączenia się
        w&nbsp;tę&nbsp;inicjatywę. Deklaracje Duchowej Adopcji w&nbsp;formie
        małych książeczek znajdują się na&nbsp;stoliku obok prasy
        katolickiej. Uroczyste złożenie przyrzeczenia, które zapoczątkuje
        duchową Adopcję, odbędzie się na&nbsp;Mszy&nbsp;św.
        o&nbsp;godz.&nbsp;18.00 w&nbsp;naszym kościele parafialnym
        25&nbsp;marca, tj.&nbsp;w&nbsp;najbliższy piątek. Msze&nbsp;św.
        w&nbsp;tym dniu o&nbsp;godz.&nbsp;6.30 , 9.00 i&nbsp;o&nbsp;18.00.</p>
        <p>5. W&nbsp;związku z&nbsp;trwającą agresją zbrojną w&nbsp;Ukrainie,
        dnia 25&nbsp;marca&nbsp;br., w&nbsp;Uroczystość Zwiastowania
        Pańskiego, Ojciec Święty Franciszek dokona Aktu Poświęcenia Rosji
        i&nbsp;Ukrainy Niepokalanemu Sercu Maryi. Jednocześnie Papież prosi,
        aby pasterze i&nbsp;wierni na&nbsp;całym świecie trwali w&nbsp;tym
        samym czasie zjednoczeni na&nbsp;modlitwie, pod przewodnictwem
        Biskupa Rzymu. W&nbsp;związku z&nbsp;powyższym w&nbsp;naszym kościele
        o&nbsp;wspomnianej godzinie wznosząc modlitwy do&nbsp;Boga, odmówimy
        Akt Poświęcenia Rosji i&nbsp;Ukrainy. Potem Droga Krzyżowa, a&nbsp;po
        Niej Msza&nbsp;św. w&nbsp;intencji pokoju na&nbsp;Ukrainie
        i&nbsp;nawrócenia Rosji. Zatem zapraszamy w&nbsp;najbliższy piątek
        na&nbsp;godzinę 17.00 do&nbsp;naszego kościoła.</p>
        <p>6. Do&nbsp;nabycia w&nbsp;zakrystii Baranki wielkanocne
        w&nbsp;cenie 8&nbsp;zł.</p>
        <p>7. Zachęcamy do&nbsp;nabycia i&nbsp;lektury katolickiej prasy.
        Gazety na&nbsp;stoliku pod chórem.</p>
        <p>8. Dziękujemy za&nbsp;modlitwę, wszelką pomoc, składane ofiary
        na&nbsp;potrzeby parafii i&nbsp;nie tylko. Wszystkim uczestniczącym
        we&nbsp;Mszy&nbsp;św. życzymy błogosławionej niedzieli.</p>
