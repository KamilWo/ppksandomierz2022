        <p>1. W&nbsp;dniach od&nbsp;4&nbsp;do&nbsp;12 sierpnia br. odbędzie
        się XXXIX Piesza Pielgrzymka Ziemi Sandomierskiej na&nbsp;Jasną Górę
        pod hasłem „Maryja znakiem nadziei i&nbsp;pokoju”. Pielgrzymka
        ma&nbsp;charakter wędrownych rekolekcji z&nbsp;programem różnych
        modlitw, konferencji i&nbsp;śpiewu. Jest zarazem jedną z&nbsp;dróg
        prowadzących do&nbsp;Pana Boga poprzez wstawiennictwo Maryi.
        Na&nbsp;pątniczy szlak zapraszamy ludzi dobrej woli, starszych
        i&nbsp;młodych. Wpisowe 150&nbsp;zł od&nbsp;osoby, druga osoba
        z&nbsp;rodziny 100&nbsp;zł, trzecia i&nbsp;następne 70&nbsp;zł.
        Przewodnikiem grupy sandomierskiej jest ks.&nbsp;Krystian Kałuża
        – wikariusz w&nbsp;naszej parafii. Dla osób, które z&nbsp;różnych
        przyczyn nie będą mogły wziąć udział w&nbsp;9-dniowej pielgrzymce
        będzie możliwość 1-dniowego sztafetowego pielgrzymowania (koszt:
        20&nbsp;zł), dojazd we&nbsp;własnym zakresie. Więcej informacji
        u&nbsp;Księdza Krystiana – telefon kontaktowy na&nbsp;plakacie
        w&nbsp;gablocie parafialnej. Zachęcamy również tych, którzy nie mogą
        wziąć udziału w&nbsp;pieszej pielgrzymce do&nbsp;udziału
        w&nbsp;duchowym pielgrzymowaniu i&nbsp;łączeniu się każdego dnia
        z&nbsp;pielgrzymami przez modlitwę. Spotkanie pielgrzymów I&nbsp;grupy
        odbędzie się w&nbsp;naszej parafii, w&nbsp;niedzielę 31&nbsp;lipca
        o&nbsp;godz.&nbsp;11.15 – najpierw Msza&nbsp;św., a&nbsp;po&nbsp;niej
        spotkanie. Zapisy na&nbsp;pielgrzymkę odbędą się przy kościele
        św.&nbsp;Pawła w&nbsp;Sandomierzu 3&nbsp;sierpnia w&nbsp;godzinach
        od&nbsp;10 do&nbsp;17.00. Bagaże składamy przy zapisach.</p>
        <p>2. Diecezjalne studium Życia Rodziny w&nbsp;Sandomierzu zaprasza
