        <h3>24.&nbsp;kwietnia 2022&nbsp;r.</h3>
        <h3>II Niedziela Wielkanocna – Niedziela Miłosierdzia Bożego, Rok C</h3>
    </div>
    <div class="col">
        <p>1. Od&nbsp;przyszłej niedzieli rozpoczynamy nabożeństwa majowe
        w&nbsp;niedziele, święta i&nbsp;dni powszednie
        o&nbsp;godz.&nbsp;17.30.</p>
        <p>2. Od&nbsp;dziś do&nbsp;soboty trwa Tydzień Miłosierdzia.
        W&nbsp;przyszłą niedzielę zbiórka do&nbsp;puszek na&nbsp;Caritas.
        W&nbsp;tym czasie szczególnie módlmy się „Koronką
        do&nbsp;Miłosierdzia Bożego”.</p>
        <p>3. Dziękujemy grupie dekoracyjnej za&nbsp;pracę przy demontażu
        dekoracji ciemnicy i&nbsp;grobu Pańskiego w&nbsp;naszym kościele
        parafialnym. Wdzięczni jesteśmy stolarzowi, Panu Wacławowi
        Miszczakowi za&nbsp;zrobienie pięknej szafy do&nbsp;przechowywania
        obrusów ołtarzowych. Wszystkim serdeczne <em>Bóg zapłać</em>.</p>
        <p>4. W&nbsp;najbliższą sobotę od&nbsp;godz.&nbsp;10.00
        do&nbsp;15.00, tzw. Dzień Otwartej Furty w&nbsp;Wyższym Seminarium
        Duchownym w&nbsp;Sandomierzu, które zaprasza do&nbsp;udziału
        w&nbsp;tym wydarzeniu dzieci i&nbsp;młodzież z&nbsp;całej diecezji.
        Na&nbsp;program składają się Msza&nbsp;św. o&nbsp;godz.&nbsp;10.00
        i&nbsp;także niespodzianki, atrakcje i&nbsp;zabawy dla
        uczestników.</p>
        <p>5. Także w&nbsp;najbliższą sobotę w&nbsp;Instytucje Teologicznym
        w&nbsp;Sandomierzu ul.&nbsp;Mariacka&nbsp;9, o&nbsp;godz.&nbsp;16.00
        będzie wykład otwarty z&nbsp;filozofii pt. „Świat Fizyki i&nbsp;Świat
        Teologii” poprowadzi ks.&nbsp;Łukasz Sadłocha. Zapraszamy.</p>
        <p>6. Dziś o&nbsp;godz.&nbsp;15.00 Mszą&nbsp;św. w&nbsp;kościele
        M.B.&nbsp;Królowej Polski w&nbsp;Sandomierzu włączymy się
        w&nbsp;inaugurację Kongresu Eucharystycznego w&nbsp;kościele
        Sandomierskim, który potrwa do&nbsp;26 czerwca&nbsp;br. Poszczególne
        parafie są&nbsp;stacjami kongresowymi. Od&nbsp;parafii
        do&nbsp;parafii będzie wędrował Najśw. Sakrament w&nbsp;monstrancji.
        Do&nbsp;nas, do&nbsp;naszego kościoła przybędzie od&nbsp;parafii
        św.&nbsp;Pawła w&nbsp;dniu 27&nbsp;kwietnia br.,
        tj.&nbsp;w&nbsp;środę o&nbsp;godz.&nbsp;17.30. O&nbsp;tej godzinie
        będziemy oczekiwali na&nbsp;przybycie Najśw. Sakramentu przed
        kościołem, następnie procesyjnie z&nbsp;Najśw. Sakramentem
        przejdziemy wokół naszego kościoła, po&nbsp;wejściu do&nbsp;kościoła
        krótka adoracja i&nbsp;błogosławieństwo Najśw. Sakramentem.
        Po&nbsp;Mszy&nbsp;św. o&nbsp;godz.&nbsp;18.00 dalszy ciąg adoracji
        z&nbsp;udziałem wiernych do&nbsp;22.00. W&nbsp;czwartek adoracja
        Najśw. Sakramentu o&nbsp;godz.&nbsp;7.00 do&nbsp;16. 45
        i&nbsp;przekazanie Najśw. Sakramentu w&nbsp;monstrancji
        do&nbsp;parafii św.&nbsp;Józefa w&nbsp;Sandomierzu. Na&nbsp;godziny
        adoracji zapraszamy parafian. Byłoby bardzo źle, gdyby którejś
        adoracyjnej godziny nie było nikogo z&nbsp;parafian w&nbsp;kościele.
        Bardzo prosimy o&nbsp;obecność i&nbsp;modlitwę. Jest wiele spraw
        w&nbsp;naszych sercach, rodzinach i&nbsp;w&nbsp;świecie, które
        należałoby polecić Bożej Miłości. Młodzież przygotowująca się
        do&nbsp;Bierzmowania z&nbsp;klas ósmych, zapraszamy na&nbsp;środę
        na&nbsp;Mszę&nbsp;św. o&nbsp;godz.&nbsp;18.00. Obecność
        obowiązkowa.</p>
        <p>7. Zachęcamy do&nbsp;nabycia i&nbsp;lektury katolickiej prasy.
        Gazety są&nbsp;na&nbsp;stoliku pod chórem.</p>
        <p>8. Dziękujemy parafianom za&nbsp;modlitwę, wszelką pomoc, składane
        ofiary na&nbsp;potrzeby parafii, przejawy szacunku i&nbsp;sympatii.
        Wszystkim życzymy błogosławionej Niedzieli.</p>
